    void draw_text_5x7( Surface& s, Vec2f pos, int scale, ColorU8_sRGB col, std::string_view txt )
    {
        float x = pos.x;
        for( char ch : txt )
        {
            auto glyph = glyph5x7_( ch );
            for( int r = 0; r < 7; ++r )
            {
                for( int c = 0; c < 5; ++c )
                {
                    if( glyph[r * 5 + c] == '1' )
                    {
                        int rr = 6 - r;
                        Vec2f p0{ x + float(c * scale), pos.y + float(rr * scale) };
                        Vec2f p1{ p0.x + float(scale), p0.y + float(scale) };
                        draw_rectangle_solid( s, p0, p1, col );
                    }
                }
            }
            x += float(6 * scale);
        }
    }
